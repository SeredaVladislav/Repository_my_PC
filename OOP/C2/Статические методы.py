# ---------------------------------------------------Cтатические методы-------------------------------------------------

# Статические методы — это методы, которые относятся сразу ко всем объектам класса, и могут вызываться вне конкретного
# объекта, обращаясь к классу напрямую.

class StaticClass:

    @staticmethod  # помечаем метод который мы хотим сделать статичным, встроенным декоратором @staticmethod
    def bar():
        print("bar")


StaticClass.bar()


# ----------------------------------------------------------------------------------------------------------------------
class StaticClass:

    @staticmethod
    def bar():
        print("bar")


f = StaticClass()
f.bar()  # вызывать статические методы через объекты так же никто не запрещает


